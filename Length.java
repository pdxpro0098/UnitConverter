public class Length {

    public double meterToKilometer(double m) {
        return m / 1000;
    }

    public double kilometerToMeter(double km) {
        return km * 1000;
    }

    public double meterToCentimeter(double m) {
        return m * 100;
    }

    public double centimeterToMeter(double cm) {
        return cm / 100;
    }
}
