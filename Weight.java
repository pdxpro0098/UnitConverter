public class Weight {

    public double kgToGram(double kg) {
        return kg * 1000;
    }

    public double gramToKg(double g) {
        return g / 1000;
    }

    public double kgToPound(double kg) {
        return kg * 2.20462;
    }

    public double poundToKg(double p) {
        return p / 2.20462;
    }
}
